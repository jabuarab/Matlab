x=[-1 0 2 ]
y= [5 2 -40]
yi=[ 12 -7 -51]
plot(x,y)

L1=@(t) (t*(t - 2))/3
L2=@(t) -((t + 1)*(t - 2))/2
L3=@(t)  (t*(t + 1))/6

Li1=@(t)   ((t-2)+t)/3
Li2=@(t)  -(t-2 + t+1)/2
Li3 =@(t)  (t+1 +t)/6



h1=@(t) (1 -2*Li1(x(1))*(t-x(1)))*(L1(t)^2)
h2=@(t) (1 -2*Li2(x(2))*(t-x(2)))*(L2(t)^2)
h3=@(t) (1 -2*Li3(x(3))*(t-x(3)))*(L3(t)^2)

hi1=@(t) (t-x(1))*(L1(t)^2)
hi2=@(t) (t-x(2))*(L2(t)^2)
hi3=@(t) (t-x(3))*(L3(t)^2) 

p=@(t) y(1)*h1(t) + yi(1)*hi1(t)+y(2)*h2(t) + yi(2)*hi2(t)+y(3)*h3(t) + yi(3)*hi3(t)
fplot(p,[-1 2]),hold on
grid on
plot(x,y,'xr')

